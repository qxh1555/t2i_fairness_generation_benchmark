#!/usr/bin/env python3
"""
Classify face attributes with FairFace from SCRFD face detections.

Input:
  SCRFD JSON generated by check/detect_faces_scrfd.py, usually:
  outputs/images/{occupation}/scrfd/all_face_results.json

Output:
  JSON files with eval_plan_fields.face_attribute_classifier.

Example:
  python check/detect_face_attributes_fairface.py \
    --scrfd-results outputs/images/nurse/scrfd/all_face_results.json \
    --weights /path/to/fairface_alldata_4race_20191111.pt \
    --race-mode 4 \
    --output-dir outputs/images/nurse/fairface \
    --save-crops
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Any, Dict, List

import torch
import torch.nn as nn
from PIL import Image
from torchvision import models, transforms


RACE_LABELS_7 = [
    "White",
    "Black",
    "Latino_Hispanic",
    "East Asian",
    "Southeast Asian",
    "Indian",
    "Middle Eastern",
]
RACE_LABELS_4 = ["White", "Black", "Asian", "Indian"]
GENDER_LABELS = ["Male", "Female"]
AGE_LABELS = ["0-2", "3-9", "10-19", "20-29", "30-39", "40-49", "50-59", "60-69", "70+"]


def load_scrfd_results(path: Path) -> List[Dict[str, Any]]:
    data = json.loads(path.read_text(encoding="utf-8"))
    if isinstance(data, list):
        return data
    if isinstance(data, dict):
        return [data]
    raise ValueError(f"Unsupported SCRFD result format: {path}")


def race_labels_for_mode(race_mode: str) -> List[str]:
    if race_mode == "7":
        return RACE_LABELS_7
    if race_mode == "4":
        return RACE_LABELS_4
    raise ValueError(f"Unsupported race mode: {race_mode}")


def output_dim_for_mode(race_mode: str) -> int:
    return len(race_labels_for_mode(race_mode)) + len(GENDER_LABELS) + len(AGE_LABELS)


def checkpoint_output_dim(state_dict: Dict[str, Any]) -> int:
    fc_weight = state_dict.get("fc.weight")
    if fc_weight is None:
        raise ValueError("Cannot infer FairFace output dimension because checkpoint has no `fc.weight`.")
    return int(fc_weight.shape[0])


def load_checkpoint_state(weights_path: Path, device: torch.device) -> Dict[str, Any]:
    checkpoint = torch.load(str(weights_path), map_location=device)
    if isinstance(checkpoint, dict):
        state_dict = checkpoint.get("state_dict") or checkpoint.get("model_state_dict") or checkpoint
    else:
        state_dict = checkpoint

    normalized_state = {}
    for key, value in state_dict.items():
        if key.startswith("module."):
            key = key[len("module.") :]
        normalized_state[key] = value
    return normalized_state


def infer_race_mode_from_state_dict(state_dict: Dict[str, Any], weights_path: Path) -> str:
    """Infer race mode when possible.

    FairFace's official 4-race checkpoint also uses an 18-dimensional fc layer.
    Therefore output dimension alone cannot distinguish 4-race from 7-race.
    """
    out_dim = checkpoint_output_dim(state_dict)
    filename = weights_path.name.lower()
    if "4race" in filename or "race4" in filename or "4_race" in filename:
        return "4"
    if out_dim == output_dim_for_mode("4"):
        return "4"
    if out_dim == output_dim_for_mode("7"):
        print(
            "[WARN] checkpoint output dimension is 18, which is ambiguous for FairFace. "
            "Assuming 7-race because the filename does not contain `4race`."
        )
        return "7"
    raise ValueError(f"Unsupported FairFace output dimension {out_dim}; expected 15 or 18.")


def build_fairface_model(weights_path: Path, device: torch.device, race_mode: str) -> tuple[nn.Module, str]:
    state_dict = load_checkpoint_state(weights_path, device)
    out_dim = checkpoint_output_dim(state_dict)
    inferred_race_mode = infer_race_mode_from_state_dict(state_dict, weights_path)
    resolved_race_mode = inferred_race_mode if race_mode == "auto" else race_mode

    if race_mode != "auto" and race_mode != inferred_race_mode:
        print(f"[WARN] requested --race-mode {race_mode}, while filename/dimension suggests {inferred_race_mode}. Using requested mode.")
    if resolved_race_mode == "7" and out_dim < output_dim_for_mode("7"):
        raise ValueError(f"7-race mode requires at least 18 outputs, got {out_dim}.")
    if resolved_race_mode == "4" and out_dim < 4:
        raise ValueError(f"4-race mode requires at least 4 outputs, got {out_dim}.")

    model = models.resnet34(weights=None)
    model.fc = nn.Linear(model.fc.in_features, out_dim)

    model.load_state_dict(state_dict, strict=True)
    model.to(device)
    model.eval()
    return model, resolved_race_mode


def choose_device(value: str) -> torch.device:
    if value == "auto":
        return torch.device("cuda" if torch.cuda.is_available() else "cpu")
    return torch.device(value)


def crop_face(image: Image.Image, bbox: List[int], padding: float) -> Image.Image:
    width, height = image.size
    x1, y1, x2, y2 = [float(x) for x in bbox]
    box_w = max(1.0, x2 - x1)
    box_h = max(1.0, y2 - y1)
    pad_x = box_w * padding
    pad_y = box_h * padding
    left = max(0, int(round(x1 - pad_x)))
    top = max(0, int(round(y1 - pad_y)))
    right = min(width, int(round(x2 + pad_x)))
    bottom = min(height, int(round(y2 + pad_y)))
    return image.crop((left, top, right, bottom))


def classifier_transform():
    return transforms.Compose(
        [
            transforms.Resize((224, 224)),
            transforms.ToTensor(),
            transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225]),
        ]
    )


def softmax_probs(logits: torch.Tensor) -> List[float]:
    probs = torch.softmax(logits, dim=0).detach().cpu().tolist()
    return [float(x) for x in probs]


def top_label(labels: List[str], probs: List[float]) -> tuple[str, float, Dict[str, float]]:
    best_idx = max(range(len(probs)), key=lambda idx: probs[idx])
    return labels[best_idx], probs[best_idx], {label: probs[idx] for idx, label in enumerate(labels)}


def normalize_gender(label: str) -> str:
    lowered = label.strip().lower()
    if lowered == "male":
        return "male"
    if lowered == "female":
        return "female"
    return "unknown"


def classify_crop(
    model: nn.Module,
    transform,
    crop: Image.Image,
    device: torch.device,
    race_mode: str,
) -> Dict[str, Any]:
    race_labels = race_labels_for_mode(race_mode)
    race_dim = len(race_labels)

    tensor = transform(crop.convert("RGB")).unsqueeze(0).to(device)
    with torch.no_grad():
        output = model(tensor).squeeze(0)

    out_dim = int(output.numel())
    if race_mode == "7":
        gender_start = 7
        age_start = 9
        head_layout = "fairface_7race_18dim"
        gender_age_available = True
    elif out_dim >= 18:
        # Official FairFace 4-race weights keep an 18-dim head. In this layout
        # race4 uses outputs[:4], logits 4:7 are not exposed as race labels,
        # and gender/age keep the same offsets as the 7-race model.
        gender_start = 7
        age_start = 9
        head_layout = "fairface_4race_18dim"
        gender_age_available = True
    else:
        gender_start = 4
        age_start = 6
        head_layout = "fairface_4race_15dim"
        gender_age_available = True

    if gender_age_available and age_start + len(AGE_LABELS) > out_dim:
        raise ValueError(
            f"Checkpoint output dimension {out_dim} is too small for {head_layout}: "
            f"need {age_start + len(AGE_LABELS)} outputs."
        )

    race_probs = softmax_probs(output[:race_dim])
    race_label, race_conf, race_dist = top_label(race_labels, race_probs)

    if gender_age_available:
        gender_probs = softmax_probs(output[gender_start:age_start])
        age_probs = softmax_probs(output[age_start : age_start + len(AGE_LABELS)])
        gender_label, gender_conf, gender_dist = top_label(GENDER_LABELS, gender_probs)
        age_label, age_conf, age_dist = top_label(AGE_LABELS, age_probs)
        perceived_gender = normalize_gender(gender_label)
        perceived_age_group = age_label
    else:
        gender_label = "unknown"
        gender_conf = None
        gender_dist = {}
        age_label = "unknown"
        age_conf = None
        age_dist = {}
        perceived_gender = "unknown"
        perceived_age_group = "unknown"

    return {
        "perceived_gender": perceived_gender,
        "gender_label_raw": gender_label,
        "gender_confidence": gender_conf,
        "gender_probs": {key.lower(): value for key, value in gender_dist.items()},
        "perceived_race_or_skin_tone": race_label,
        "race_mode": race_mode,
        "head_layout": head_layout,
        "gender_age_available": gender_age_available,
        "logit_layout": {
            "race": [0, race_dim],
            "gender": [gender_start, age_start] if gender_age_available else None,
            "age": [age_start, age_start + len(AGE_LABELS)] if gender_age_available else None,
            "ignored": [4, 7] if head_layout == "fairface_4race_18dim" else [],
        },
        "race_confidence": race_conf,
        "race_probs": race_dist,
        "perceived_age_group": perceived_age_group,
        "age_confidence": age_conf,
        "age_probs": age_dist,
    }


def classify_scrfd_results(
    model: nn.Module,
    transform,
    scrfd_results: List[Dict[str, Any]],
    output_dir: Path,
    device: torch.device,
    crop_padding: float,
    save_crops: bool,
    race_mode: str,
) -> List[Dict[str, Any]]:
    output_dir.mkdir(parents=True, exist_ok=True)
    crops_dir = output_dir / "crops"
    if save_crops:
        crops_dir.mkdir(parents=True, exist_ok=True)

    all_results: List[Dict[str, Any]] = []
    for image_result in scrfd_results:
        image_path = Path(image_result["image_path"])
        image = Image.open(image_path).convert("RGB")
        faces_out = []

        for face in image_result.get("faces", []):
            crop = crop_face(image, face["bbox"], padding=crop_padding)
            attr = classify_crop(model, transform, crop, device, race_mode=race_mode)
            face_out = {
                "face_id": face.get("face_id"),
                "bbox": face.get("bbox"),
                "det_score": face.get("det_score"),
                "clear_face": face.get("clear_face"),
                **attr,
            }
            if save_crops:
                crop_path = crops_dir / f"{image_path.stem}_face_{face.get('face_id', 0)}.png"
                crop.save(crop_path)
                face_out["crop_path"] = str(crop_path)
            faces_out.append(face_out)

        result = {
            "image_path": str(image_path),
            "image_name": image_path.name,
            "source_face_count": image_result.get("face_count", len(image_result.get("faces", []))),
            "source_clear_face_count": image_result.get("clear_face_count"),
            "classified_face_count": len(faces_out),
            "faces": faces_out,
            "eval_plan_fields": {
                "face_attribute_classifier": {
                    "race_mode": race_mode,
                    "faces": [
                        {
                            "face_id": face["face_id"],
                            "bbox": face["bbox"],
                            "perceived_gender": face["perceived_gender"],
                            "gender_confidence": face["gender_confidence"],
                            "perceived_race_or_skin_tone": face["perceived_race_or_skin_tone"],
                            "race_confidence": face["race_confidence"],
                            "perceived_age_group": face["perceived_age_group"],
                            "age_confidence": face["age_confidence"],
                        }
                        for face in faces_out
                    ]
                }
            },
        }

        out_json_path = output_dir / f"{image_path.stem}_fairface.json"
        out_json_path.write_text(json.dumps(result, ensure_ascii=False, indent=2), encoding="utf-8")
        print(f"[OK] {image_path.name}: classified {len(faces_out)} face(s)")
        print(f"     json result: {out_json_path}")
        all_results.append(result)

    all_path = output_dir / "all_face_attribute_results.json"
    all_path.write_text(json.dumps(all_results, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"\n[DONE] classified images: {len(all_results)}")
    print(f"[DONE] all results saved to: {all_path}")
    return all_results


def main() -> None:
    parser = argparse.ArgumentParser(description="Classify SCRFD face crops with FairFace.")
    parser.add_argument("--scrfd-results", required=True, help="SCRFD all_face_results.json or one *_faces.json")
    parser.add_argument("--weights", required=True, help="Path to FairFace ResNet34 .pt weights")
    parser.add_argument("--output-dir", required=True, help="Directory for FairFace JSON outputs")
    parser.add_argument("--device", default="auto", help="auto, cpu, cuda, cuda:0, ...")
    parser.add_argument("--race-mode", choices=["auto", "4", "7"], default="auto", help="FairFace race head: auto, 4, or 7. auto uses filename hints such as `4race` because FairFace 4-race weights can still have an 18-dim head.")
    parser.add_argument("--crop-padding", type=float, default=0.25, help="Relative bbox padding before FairFace crop")
    parser.add_argument("--save-crops", action="store_true", help="Save cropped face images")
    args = parser.parse_args()

    device = choose_device(args.device)
    print(f"device: {device}")
    scrfd_results = load_scrfd_results(Path(args.scrfd_results))
    model, resolved_race_mode = build_fairface_model(Path(args.weights), device, race_mode=args.race_mode)
    print(f"race mode: {resolved_race_mode}")
    transform = classifier_transform()
    classify_scrfd_results(
        model=model,
        transform=transform,
        scrfd_results=scrfd_results,
        output_dir=Path(args.output_dir),
        device=device,
        crop_padding=args.crop_padding,
        save_crops=args.save_crops,
        race_mode=resolved_race_mode,
    )


if __name__ == "__main__":
    main()
